		<span class='details' >
		This project created an interactive video component for the performance of Steve Heitzeg's Nobel Symphony.  It was performed at Minneapolis Orchestra Hall on April 18, 2004.  This was my senior project, and I worked with Piotr Szyhalski as his teaching / design assistant for a class called Interactive Events Development. <br /><br />The video was partially driven by audience interaction from a physical installation in the lobby of the hall and a flash based web site, as well as input from micd percussive instruments.  Nearly all of the video footage was shot by members of the class, with the remainder coming from other academic or non-profit sources. A printed program explained the experience to the audience.

</span></p>
	

	<p class='news_post'>
		The Program<span class='details' >
	I co-designed the program that was handed out to all visitors to the hall for the event.  I worked on the overall conception of the piece and did the initial layout, as well as the final layout for the
	visual score inside.  Matt assisted with photography and much of the detail work while I was busy finishing the projection for the event.
