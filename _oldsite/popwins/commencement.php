		<p class='news_post'>Notes
		<span class='details'>
		Mark Fox and I created this presentation in lieu of the typical Commencement Address that a BFA speaker gives at the ceremony each spring.  We wanted to focus our speech on two major points.  First, 
		we wanted to replace the blank faux diplomas handed out in the ceremony with something more real, that we as graduates would give to each other, rather than an institution giving to us.  We (as a group of graduates)
		learned more from each other than from teachers or the institution, so it was a symbolic gesture in that manner.  Additionally, we wanted to focus on how an artist should proceed in our turbulent world
		from that moment forth; how an artist should strive to exist and find their place in society in the world.  This portion of the speech was influenced by a speech <a href="http://www.artic.edu/saic/programs/faculty/facbios.html#cbecker" target="_blank">Carol Becker</a>
		gave in 1990 entitled <a href="http://archives.mcad.edu/POLITPROP/palace/library/socialresp.html" target="_blank">Social Responsibility and the Place of the Artist in Society</a>. Much like Becker's speech, we wanted to give one last emphasis to the graduates and their families that Art exists
		to create dialogue and understanding.</span></p>
		<p class='news_post'>
		<span class='details'>
		Additionally, there was an interactive component to the performance.  While Mark was giving the speech, I was hand certifying our diplomas.  The stamping action triggered a simple replacement of negative
		words with positive words.  The act of stamping and creating a new certified artist replaced a bad element in the display (a word) with a positive one.  Mark Fox and I co-wrote the speech.  Beth Van Dam
		assisted in editing of the speech.  Mark designed the document and I designed the interactive component. Thanks to my father for recording the event.
</span></p>

